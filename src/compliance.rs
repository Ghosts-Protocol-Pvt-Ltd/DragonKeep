// ─── Enums ───────────────────────────────────────────────────────────────────

/// Supported compliance frameworks.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum ComplianceFramework {
    CIS,
    STIG,
    NIST80053,
    MITRE,
    #[serde(rename = "PCI_DSS")]
    PCI_DSS,
    HIPAA,
    SOC2,
}

impl fmt::Display for ComplianceFramework {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::CIS => write!(f, "CIS Benchmarks"),
            Self::STIG => write!(f, "DISA STIG"),
            Self::NIST80053 => write!(f, "NIST SP 800-53 Rev 5"),
            Self::MITRE => write!(f, "MITRE ATT&CK"),
            Self::PCI_DSS => write!(f, "PCI DSS v4.0"),
            Self::HIPAA => write!(f, "HIPAA Security Rule"),
            Self::SOC2 => write!(f, "SOC 2 Type II"),
        }
    }
}

/// Severity classification for a compliance control.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum ControlSeverity {
    Required,
    Recommended,
    Optional,
}

impl fmt::Display for ControlSeverity {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::Required => write!(f, "Required"),
            Self::Recommended => write!(f, "Recommended"),
            Self::Optional => write!(f, "Optional"),
        }
    }
}

/// Evaluation status for a single control.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum ControlStatus {
    Pass,
    Fail,
    NotApplicable,
    NotChecked,
}

impl fmt::Display for ControlStatus {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::Pass => write!(f, "PASS"),
            Self::Fail => write!(f, "FAIL"),
            Self::NotApplicable => write!(f, "N/A"),
            Self::NotChecked => write!(f, "NOT CHECKED"),
        }
    }
}

impl ControlStatus {
    pub fn colored_label(&self) -> String {
        match self {
            Self::Pass => "PASS".green().bold().to_string(),
            Self::Fail => "FAIL".red().bold().to_string(),
            Self::NotApplicable => "N/A".bright_black().to_string(),
            Self::NotChecked => "NOT CHECKED".yellow().to_string(),
        }
    }
}

// ─── Core structs ────────────────────────────────────────────────────────────

/// A single compliance control within a framework.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ComplianceControl {
    pub id: String,
    pub title: String,
    pub description: String,
    pub severity: ControlSeverity,
    pub finding_rules: Vec<String>,
    pub framework_ref: String,
}

/// A compliance template: a framework + its set of controls.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ComplianceTemplate {
    pub framework: ComplianceFramework,
    pub name: String,
    pub version: String,
    pub controls: Vec<ComplianceControl>,
}

/// The result of evaluating one control against scan findings.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ControlResult {
    pub control: ComplianceControl,
    pub status: ControlStatus,
    pub findings: Vec<Finding>,
    pub evidence: Option<String>,
}

/// Full compliance report generated by [`check_compliance`].
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ComplianceReport {
    pub framework: ComplianceFramework,
    pub generated_at: String,
    pub hostname: String,
    pub total_controls: usize,
    pub passed: usize,
    pub failed: usize,
    pub not_applicable: usize,
    pub not_checked: usize,
    pub compliance_score: f32,
    pub results: Vec<ControlResult>,
}

// ─── Compliance checking engine ──────────────────────────────────────────────

/// Evaluate every control in `template` against the supplied `findings`.
///
/// Matching logic (checked in order):
/// 1. `Finding::rule_id` matches any entry in `ComplianceControl::finding_rules`
/// 2. `Finding::cis_id` matches the control id (for CIS controls)
/// 3. `Finding::stig` matches the control id (for STIG controls)
/// 4. `Finding::nist` contains the control id (for NIST controls)
///
/// Status derivation from matched findings:
/// - If any finding has `Severity::Pass` → `ControlStatus::Pass`
/// - If any finding has `Severity::Critical` or `Severity::High` → `ControlStatus::Fail`
/// - If any finding has `Severity::Warning` → `ControlStatus::Fail`
/// - If findings matched but all are `Severity::Info` → `ControlStatus::Pass`
/// - No findings matched → `ControlStatus::NotChecked`
pub fn check_compliance(template: &ComplianceTemplate, findings: &[Finding]) -> ComplianceReport {
    let hostname = hostname();

    let mut results = Vec::with_capacity(template.controls.len());
    let mut passed: usize = 0;
    let mut failed: usize = 0;
    let mut not_applicable: usize = 0;
    let mut not_checked: usize = 0;

    for control in &template.controls {
        let matched: Vec<Finding> = findings
            .iter()
            .filter(|f| finding_matches_control(f, control))
            .cloned()
            .collect();

        let (status, evidence) = if matched.is_empty() {
            (ControlStatus::NotChecked, None)
        } else {
            derive_status(&matched)
        };

        match status {
            ControlStatus::Pass => passed += 1,
            ControlStatus::Fail => failed += 1,
            ControlStatus::NotApplicable => not_applicable += 1,
            ControlStatus::NotChecked => not_checked += 1,
        }

        results.push(ControlResult {
            control: control.clone(),
            status,
            findings: matched,
            evidence,
        });
    }

    let total = template.controls.len();
    let evaluated = passed + failed;
    let score = if evaluated > 0 {
        (passed as f32 / evaluated as f32) * 100.0
    } else {
        0.0
    };

    ComplianceReport {
        framework: template.framework.clone(),
        generated_at: Utc::now().to_rfc3339(),
        hostname,
        total_controls: total,
        passed,
        failed,
        not_applicable,
        not_checked,
        compliance_score: (score * 10.0).round() / 10.0,
        results,
    }
}

fn finding_matches_control(finding: &Finding, control: &ComplianceControl) -> bool {
    // 1. rule_id match
    if let Some(ref rule_id) = finding.rule_id {
        if control.finding_rules.iter().any(|r| r == rule_id) {
            return true;
        }
    }

    // 2. CIS ID match
    if let Some(ref cis_id) = finding.cis_id {
        if control.id.ends_with(cis_id) || cis_id == &control.id {
            return true;
        }
    }

    // 3. STIG match
    if let Some(ref stig) = finding.stig {
        if stig == &control.id {
            return true;
        }
    }

    // 4. NIST match
    if let Some(ref nist_controls) = finding.nist {
        if nist_controls.iter().any(|n| n == &control.id) {
            return true;
        }
    }

    false
}

fn derive_status(matched: &[Finding]) -> (ControlStatus, Option<String>) {
    let has_pass = matched.iter().any(|f| f.severity == Severity::Pass);
    let has_critical = matched.iter().any(|f| f.severity == Severity::Critical);
    let has_high = matched.iter().any(|f| f.severity == Severity::High);
    let has_warning = matched.iter().any(|f| f.severity == Severity::Warning);

    if has_critical || has_high {
        let titles: Vec<&str> = matched
            .iter()
            .filter(|f| f.severity == Severity::Critical || f.severity == Severity::High)
            .map(|f| f.title.as_str())
            .collect();
        (
            ControlStatus::Fail,
            Some(format!("Failed: {}", titles.join("; "))),
        )
    } else if has_warning {
        let titles: Vec<&str> = matched
            .iter()
            .filter(|f| f.severity == Severity::Warning)
            .map(|f| f.title.as_str())
            .collect();
        (
            ControlStatus::Fail,
            Some(format!("Warnings: {}", titles.join("; "))),
        )
    } else if has_pass {
        (ControlStatus::Pass, Some("All checks passed".to_string()))
    } else {
        // Only Info-level findings → treat as pass
        (ControlStatus::Pass, Some("Informational only".to_string()))
    }
}

fn hostname() -> String {
    std::env::var("HOSTNAME")
        .or_else(|_| std::env::var("HOST"))
        .unwrap_or_else(|_| "unknown".to_string())
}

// ─── Pretty-print / render ───────────────────────────────────────────────────

/// Render a compliance report in the requested output format.
pub fn render_compliance_report(report: &ComplianceReport, format: &OutputFormat) -> String {
    match format {
        OutputFormat::Json => serde_json::to_string_pretty(report).unwrap_or_default(),
        OutputFormat::Minimal => render_minimal(report),
        _ => render_pretty(report),
    }
}

fn render_pretty(report: &ComplianceReport) -> String {
    let mut out = String::new();

    // Header
    out.push_str(&format!(
        "\n{}\n",
        "╔══════════════════════════════════════════════════════════════╗"
            .bright_cyan()
    ));
    out.push_str(&format!(
        "{}  🏰 DragonKeep Compliance Report{}\n",
        "║".bright_cyan(),
        "                            ║".bright_cyan()
    ));
    out.push_str(&format!(
        "{}\n",
        "╚══════════════════════════════════════════════════════════════╝"
            .bright_cyan()
    ));

    // Summary
    out.push_str(&format!(
        "\n  {} {}\n",
        "Framework:".bold(),
        report.framework
    ));
    out.push_str(&format!("  {} {}\n", "Host:".bold(), report.hostname));
    out.push_str(&format!(
        "  {} {}\n",
        "Generated:".bold(),
        report.generated_at
    ));

    let score_colored = if report.compliance_score >= 80.0 {
        format!("{:.1}%", report.compliance_score).green().bold()
    } else if report.compliance_score >= 50.0 {
        format!("{:.1}%", report.compliance_score).yellow().bold()
    } else {
        format!("{:.1}%", report.compliance_score).red().bold()
    };
    out.push_str(&format!(
        "  {} {}\n\n",
        "Compliance Score:".bold(),
        score_colored
    ));

    out.push_str(&format!(
        "  {} {}  {} {}  {} {}  {} {}\n\n",
        "✓".green().bold(),
        format!("{} Passed", report.passed).green(),
        "✗".red().bold(),
        format!("{} Failed", report.failed).red(),
        "—".bright_black(),
        format!("{} N/A", report.not_applicable).bright_black(),
        "?".yellow(),
        format!("{} Not Checked", report.not_checked).yellow(),
    ));

    // Results table
    #[derive(Tabled)]
    struct Row {
        #[tabled(rename = "Status")]
        status: String,
        #[tabled(rename = "Control ID")]
        id: String,
        #[tabled(rename = "Title")]
        title: String,
        #[tabled(rename = "Severity")]
        severity: String,
        #[tabled(rename = "Evidence")]
        evidence: String,
    }

    let rows: Vec<Row> = report
        .results
        .iter()
        .map(|r| Row {
            status: r.status.colored_label(),
            id: r.control.id.clone(),
            title: truncate(&r.control.title, 40),
            severity: r.control.severity.to_string(),
            evidence: truncate(
                r.evidence.as_deref().unwrap_or("—"),
                50,
            ),
        })
        .collect();

    if !rows.is_empty() {
        let table = Table::new(&rows)
            .with(Style::rounded())
            .with(Modify::new(Columns::single(2)).with(Width::truncate(42).suffix("…")))
            .with(Modify::new(Columns::single(4)).with(Width::truncate(52).suffix("…")))
            .to_string();
        out.push_str(&table);
        out.push('\n');
    }

    out
}

fn render_minimal(report: &ComplianceReport) -> String {
    let mut out = String::new();
    out.push_str(&format!(
        "{} — {:.1}% ({}/{} passed)\n",
        report.framework,
        report.compliance_score,
        report.passed,
        report.passed + report.failed,
    ));
    for r in &report.results {
        if r.status == ControlStatus::Fail {
            out.push_str(&format!("  FAIL  {} — {}\n", r.control.id, r.control.title));
        }
    }
    out
}

fn truncate(s: &str, max: usize) -> String {
    if s.len() <= max {
        s.to_string()
    } else {
        format!("{}…", &s[..max - 1])
    }
}

// ─── Helper for building controls ───────────────────────────────────────────

fn ctrl(
    id: &str,
    title: &str,
    description: &str,
    severity: ControlSeverity,
    rules: &[&str],
    framework_ref: &str,
) -> ComplianceControl {
    ComplianceControl {
        id: id.to_string(),
        title: title.to_string(),
        description: description.to_string(),
        severity,
        finding_rules: rules.iter().map(|s| s.to_string()).collect(),
        framework_ref: framework_ref.to_string(),
    }
}

