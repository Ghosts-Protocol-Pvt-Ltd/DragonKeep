    #[test]
    fn compliance_score_percentage() {
        let template = ComplianceTemplate {
            framework: ComplianceFramework::CIS,
            name: "Test".to_string(),
            version: "1.0".to_string(),
            controls: vec![
                ctrl("C1", "Pass control", "d", ControlSeverity::Required, &["DK-SEN-001"], "r"),
                ctrl("C2", "Fail control", "d", ControlSeverity::Required, &["DK-SEN-002"], "r"),
                ctrl("C3", "Unchecked", "d", ControlSeverity::Required, &["DK-SEN-099"], "r"),
            ],
        };

        let findings = vec![
            Finding::pass("OK").with_rule("DK-SEN-001"),
            Finding::critical("BAD").with_rule("DK-SEN-002"),
        ];

        let report = check_compliance(&template, &findings);
        assert_eq!(report.passed, 1);
        assert_eq!(report.failed, 1);
        assert_eq!(report.not_checked, 1);
        assert!((report.compliance_score - 50.0).abs() < f32::EPSILON);
    }
